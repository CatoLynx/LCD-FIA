/**
  * @brief This function handles EXTI line4 interrupt.
  */
void EXTI4_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI4_IRQn 0 */
    if (HAL_GPIO_ReadPin(BITMAP_CS_GPIO_Port, BITMAP_CS_Pin)) {
        // Abort ongoing SPI Rx if CS goes high
        // If the transfer has already completed, nothing will happen
        FIA_AbortBitmapReceive();
    } else {
        // Start SPI Rx if CS goes low
        FIA_StartBitmapReceive();
    }
  /* USER CODE END EXTI4_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_4);
  /* USER CODE BEGIN EXTI4_IRQn 1 */

  /* USER CODE END EXTI4_IRQn 1 */
}

